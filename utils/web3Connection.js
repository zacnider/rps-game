const CONTRACT_ADDRESS = process.env.CONTRACT_ADDRESS || 'DEPLOYED_CONTRACT_ADDRESS';
const RPC_URL = process.env.RPC_URL || 'https://sepolia.infura.io/v3/YOUR_PROJECT_ID';
const PRIVATE_KEY = process.env.PRIVATE_KEY;

async function initWeb3Connection() {
    try {
        // Eğer RPC URL veya Private Key tanımlı değilse hata ver
        if (!RPC_URL || !PRIVATE_KEY) {
            throw new Error('RPC_URL veya PRIVATE_KEY tanımlanmamış');
        }

        // Provider Bağlantısı
        const provider = new ethers.providers.JsonRpcProvider(RPC_URL);
        
        // Cüzdan Oluşturma
        const wallet = new ethers.Wallet(PRIVATE_KEY, provider);
        
        // Kontrat Bağlantısı
        const contract = new ethers.Contract(
            CONTRACT_ADDRESS, 
            contractABI, 
            wallet
        );

        console.log('Web3 Bağlantısı Başarılı');
        return contract;
    } catch (error) {
        console.error('Web3 Bağlantı Hatası:', error.message);
        throw error;
    }
}

module.exports = {
    initWeb3Connection
};
